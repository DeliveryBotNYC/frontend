export const getZoneDefaults = (zoneId: number): ZoneDefault | undefined => {
  return ZONE_DEFAULTS.find((zone) => zone.zone_id === zoneId);
};

export const getZoneByName = (name: string): ZoneDefault | undefined => {
  return ZONE_DEFAULTS.find(
    (zone) => zone.name.toLowerCase() === name.toLowerCase()
  );
};
